#include "libft.h"
#include <limits.h>

void	ft_putnbr(int nb)
{
	if (nb < 0)
	{
		if (nb == INT_MIN)
		{
			ft_putstr("-2147483648");
			return ;
		}
		ft_putchar('-');
		nb = -nb;
	}
	if (nb >= 10)
	{
		ft_putnbr(nb / 10);
		ft_putnbr(nb % 10);
	}
	else
		ft_putchar('0' + nb);
}
