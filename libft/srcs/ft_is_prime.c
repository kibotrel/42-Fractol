#include "libft.h"

int	ft_is_prime(int nb)
{
	int x;

	if (nb < 0 || nb == 0 || nb == 1)
		return (0);
	if (nb == 2 || nb == 3 || nb == 5)
		return (1);
	if (nb % 2 == 0 || nb % 3 == 0 || nb % 5 == 0)
		return (0);
	x = 7;
	while (x < nb)
	{
		if (nb % x == 0)
			return (0);
		x += 2;
	}
	return (1);
}
